package Exercitii_din_fisierul_text.Curs9.Ex2.FiguriGeometrice;


public class Punct {

    private double x, y;

    public Punct ( double x, double y ) {

        this.x = x;

        this.y = y;

    }

    public double getX () {

        return x;

    }

    public double getY () {

        return y;

    }


}



